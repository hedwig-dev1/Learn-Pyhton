nilai = int(input('Masukkan nilai: '))

# nilai = int(nilai)  # Konversi input ke integer

if nilai >= 87:
  print('Nilai anda A')
elif nilai >= 78:
  print('Nilai anda AB')
elif nilai >= 68:
  print('Nilai anda B')
elif nilai >= 60:
  print('Nilai anda BC')
elif nilai >= 51:
  print('Nilai anda C')
elif nilai >= 41:
  print('Nilai anda D')
else:
  print('Nilai anda E')
