# Operator
# f-string di gunakan untuk mnyisipkan nilai variabel
a = int(input('Masukkan nilai A: '))
b = int(input('Masukkan nilai B: '))
c = int(input('Masukkan nilai C: '))

hasil_jumlah = a + b + c

# Format string untuk output yang diinginkan
output = f"Hasil penambahan {a} + {b} + {c} : {hasil_jumlah}"

print(output)
